#Rules to Assign a variable
#1. The variable should contain letters, numbers or underscores.
#2. Do not start the variable with numbers
#3. Spaces are not allowed in variable name.
#4. Do not use the function keywords as variable name.
#5. The variable name should be short and descriptive
#6. Always use lower case letters (case sensitive)

#Example:
fruit_basket="mangoes"
print(fruit_basket)
del(fruit_basket)

fruit_basket="mangoes", "oranges"
print(fruit_basket)

fruit_basket=8

print(type(fruit_basket))
